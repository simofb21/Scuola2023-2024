/*
Bubble Sort: Effettua confronti uno a uno e ha una complessità nel caso peggiore di n^2 elementi. Shake Sort se è con una sentinella.
Complessità:
- Migliore caso: O(n)
- Peggiore caso: O(n^2)

Selection Sort: Individua il minimo e lo mette in prima posizione, poi continua così finché l'array non è ordinato.
Complessità:
- Migliore caso: O(n^2)
- Peggiore caso: O(n^2)

Insertion Sort: Si inserisce un elemento nella posizione corretta nell'ordine. Se ne arriva uno che va prima, si crea uno spazio prima e si spostano gli altri.
Complessità:
- Migliore caso: O(n)
- Peggiore caso: O(n^2)

Ricerca Sequenziale: Divide sempre l'array a metà confrontando il valore medio con quello da trovare finché non viene trovato. Complessità nel peggiore  log2(n) (quando l'elemento da trovare è già a metà).

Quick Sort: Si definisce un elemento perno (pivot). Gli elementi si dividono in due gruppi: a sinistra quelli di valore inferiore e a destra quelli di valore superiore rispetto al pivot, dividendo così l'array in due sottovettori. La posizione q è quella finale del pivot.
Complessità:
- Migliore caso: O(n log n)
- Peggiore caso: O(n^2)
*/
